/*java_math_class*/

class java_math_class{
    public static void main(String[] args){
        double value_n_ve = -1238.435554;
        double value_p_ve = 1238.435554;
        
        System.out.println(Math.abs(value_n_ve));
        System.out.println(Math.ceil(value_p_ve));  //round up
        System.out.println(Math.floor(value_p_ve)); //round down
        System.out.println(Math.max(value_p_ve, value_n_ve));   //find biggest value
        System.out.println(Math.min(value_p_ve, value_n_ve));   //find lowest value
        System.out.println(Math.pow(2, 3));   // 2^3
        System.out.println(Math.sqrt(4));     // square root of 4


    }
}

