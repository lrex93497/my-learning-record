class print_and_data_type{
    public static void main(String[] args){
        
        int test_int = 1;               
        boolean test_boolean = true;       
        float test_FloatNum = 123.12f;    
        char test_Letter = 'D';         // ''
        String test_Text = "Hello!!!";     // ""    
		long test_long = 203457;
        /*  print  */
        System.out.println(test_int + "123");
        System.out.print(test_FloatNum);
        System.out.print(test_Letter+"\n");
        System.out.println(test_boolean);
        System.out.println(test_Text);
        System.out.println(test_long);


    }




}

